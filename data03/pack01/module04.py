# 기본 라이브러리(표준 라이브러리)

import math
import random

print (math.sqrt(9.0))
print (random.randint(1,100))

# 연산자
# 산술, 대입, 비교, 논리
# 산술연산자
x = 100
y = 333
print (x + y)
print (x - y)
print (x * y)
print (x / y) # 소수점자리도 나옴.
print (x // y) # 소수점을 잘라 정수까지만 값이 구해짐.

# 대입연산자
# 파이썬에서는 변수의 생성과 타입이 값을 대입할 때 결정된다.
# auto-typing!

# 비교 연산자 => 비교의 결과가 논리형
print (x == y)
print (x != y)
